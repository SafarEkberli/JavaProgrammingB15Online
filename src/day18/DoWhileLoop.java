package day18;

public class DoWhileLoop {
    public static void main(String[] args) {
        int x =1;
        do {
            System.out.println(x);
            ++x;
        }while (x<=10);
        System.out.println();
        int y = 5;
         do {
             System.out.println(y);
             --y;


         }while (y>0);

    }
}
