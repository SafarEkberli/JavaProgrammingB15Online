package day13;

public class TesttringStartWithOrEndWith {
    public static void main(String[] args) {
        String name = "Safar";

        System.out.println(name.startsWith("Sa"));
        System.out.println(name.startsWith("sd"));

        System.out.println(name.endsWith("r"));
        System.out.println(name.endsWith("R"));
        System.out.println(name.endsWith("fa"));
    }
}
